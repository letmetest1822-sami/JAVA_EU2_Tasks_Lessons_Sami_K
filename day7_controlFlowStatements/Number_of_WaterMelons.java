package day7_controlFlowStatements;

public class Number_of_WaterMelons {

	public static void main(String[] args) {
		
		int numberOfWaterMelon;
		boolean lotsOfWaterMelon;
		lotsOfWaterMelon = true;
		
		numberOfWaterMelon = 40;
		
		if (numberOfWaterMelon >= 20 ) {
			System.out.println(" I have more than 20 water melons");
		}
		
		if (lotsOfWaterMelon) {
			System.out.println(" Good Job");
		}
		
		if (lotsOfWaterMelon == false) {
			System.out.println(" I need more water melons");
		}
		
	}

}

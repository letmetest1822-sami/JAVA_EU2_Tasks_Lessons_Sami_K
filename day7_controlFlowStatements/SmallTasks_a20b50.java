package day7_controlFlowStatements;

public class SmallTasks_a20b50 {

	public static void main(String[] args) {
		/*Write an if statement that 
		 * assigns 20 to the variable a 
		 * if variable b is 50 and c is
		 * greater and equal to 100 */
		
		int a = 0;
		int b = 50;
		int c = 100;
		
		if( (b == 50) & (c>=100)) {
			a = 20;
			
			System.out.println(" a = "+ a + " b = 50 and c >= 100" );
		}

	}

}

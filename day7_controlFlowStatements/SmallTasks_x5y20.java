package day7_controlFlowStatements;

public class SmallTasks_x5y20 {

	public static void main(String[] args) {
		/*Write an if statement that assigns 5 to x 
		 * when y is equal to 20 */
		
		int x=0;
		int y=20;
		
		if (y == 20) {
			x = 5;
				
		}
		
		System.out.println("X = " + x + " because y = 20");
	}

}
